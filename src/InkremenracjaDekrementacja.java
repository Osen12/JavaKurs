public class InkremenracjaDekrementacja {

    public static void main(String[] args) {
        int a  = 0;
        System.out.println("wartosc a :" + a);
        int b = a++;
        System.out.println("wartosc b: " + b);
        System.out.println("wartosc a: " + a);
        int c = ++a;
        System.out.println("wartosc c: " + c);
        System.out.println("wartosc a: " + a);
    }
}
